
public class Season {
	public static void main(String[] args) {
		 String season = "summer";
		 
		 switch(season) {
		 
		 case "summer":
			 System.out.println("Stay hydrated");
			 break;
		 case "winter":
			 System.out.println("Wear warm cloths");
			 break;
		 case "rainy":
			 System.out.println("carry an umbrella");
			 break;
			 default:
				 System.out.println("no more seasons...");
		 }
	}

}

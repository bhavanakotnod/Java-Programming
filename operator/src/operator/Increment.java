package operator;

public class Increment {
	public static void main(String[] args)
	{
		
		//increement operator only increment by 1 value
		
		int a = 5;
		a++;
		System.out.println(a);
		
		System.out.println(a++);  // post increment
		System.out.println(a);
		
		System.out.println(++a); //  pre increment
		
		
	
	}

}

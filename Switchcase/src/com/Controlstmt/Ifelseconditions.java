package com.Controlstmt;

public class Ifelseconditions {
	public static void main(String args[])
	{
		int a = 5;
		if(a == 5)           //here condition is true therefore it prints yes
		{
			System.out.println("Yes");
		}
		else {                           // here first condition is satisfied so compiler dont come to next condion
			System.out.println("No");
		}
	}

}

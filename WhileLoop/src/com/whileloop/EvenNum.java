package com.whileloop;

public class EvenNum {
	public static void main(String[] args) {
		// initialization of i equals to zero
		int i= 0;
		//loop check every number in between  0 to 20
		while(i <= 20) {
			// check number is even or not
			if(i % 2 == 0)
			System.out.println(i);
			//increment by one
			i++;
			
		}
	}

}

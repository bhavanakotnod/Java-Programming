package com.methodparameter;

public class Hello {

	public static void main(String[] args) {
		helo("Hello,world!");
		
	}
	// return type is string
	// helo is the method name 
	//access specifier, keyword, data type, method name
	public static String helo(String name) {
		
		System.out.println(name);
		return name;
		
	}
}
